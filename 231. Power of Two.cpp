class Solution {
public:
    bool isPowerOfTwo(int n) {
        int count=0;
        while(n){
            if(n&1==1) count++;
            n>>=1;
            if(count>1){
                return 0;
                exit(0);
            }
        }
        if(count!=0)
            return 1;
        else
            return 0;
    }
};
